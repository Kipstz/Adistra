			end
		end
	end
	for k in pairs(Config['tunningsystem'].Wheels) do
		local podiri = false
		if tipom == "Tyres Front" then
			if k ~= "motorcycle" then
				podiri = true
			end
		else
			podiri = true
		end
		if podiri then
			local bool,wheel,num = TunningSystem:isWheelType(k,numeromod)
			local transla = Config['tunningsystem'].Translations[k]
			if transla == nil then
				transla = k
			end
			local roda = 999
			if bool then
				roda = wheel+1
			end
			if num >= 1 then
				table.insert(submenus, {type = json.encode({tipo = k, index = #submenus+1}),title = transla, src = "img/"..k..".png", subSubMenuSelected = roda-1, subSubMenu = TunningSystem:AddToSubMenu(k,num,numeromod)})
			end
		end
	end
	SetVehicleMod(TunningSystem.vehSelected,23, rodaatual,GetVehicleModVariation(TunningSystem.vehSelected,23))
	local menu = {
		type = json.encode({tipo = tipom}),
		title = transl,
		src = "img/"..tipom..".png",
		subMenuTitle = Config['tunningsystem'].Translations["change"].." "..Config['tunningsystem'].Translations["vehtyres"],
		subMenuSelected = 999,
		subMenu = submenus
	}
	return menu,tableza
