		type = json.encode({tipo = "extrafault",index = "deftable"}),
		title = Config['tunningsystem'].Translations["def"],
		src = "img/Default.png",
	}, }
	for id = 0, 20, 1 do
		if DoesExtraExist(TunningSystem.vehSelected, id) then
			if IsVehicleExtraTurnedOn(TunningSystem.vehSelected, id) then
				default[id] = true
			end
			table.insert(submenu,{type = json.encode({tipo = "extra", index = id}),title = Config['tunningsystem'].Translations["extra"], src = "img/extra.png"})
		end
	end
	local menu = {
		type = json.encode({tipo = "extra"}),
		title = Config['tunningsystem'].Translations["extra"],
		src = "img/".."extra.png",
		subMenuSelected = 99,
		subMenu = submenu
	}
	return menu,default
end