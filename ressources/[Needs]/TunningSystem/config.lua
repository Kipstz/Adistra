function GetTyres(tipom)
	local numeromod = Config['tunningsystem'].TunningMods[tipom]
	local submenus = {}
	local transl = Config['tunningsystem'].Translations[tipom]
	if transl == nil then
		transl = tipom
	end
	SetVehicleMod(TunningSystem.vehSelected,23, -1,GetVehicleModVariation(TunningSystem.vehSelected,23))
	local tableza = {}
	for k in pairs(Config['tunningsystem'].Wheels) do
		local bool,wheel,num = TunningSystem:isWheelType(k,numeromod)
		if bool then
			local transla = Config['tunningsystem'].Translations[k]
			if transla == nil then
				transla = k
			end
			tableza = {rodadefault=wheel,tipo=k}
			if tipom == "Tyres Front" then
				table.insert(submenus, {type = json.encode({tipo = "rodadefault", index = {tipo = k, roda = wheel,mota=23}}),title = Config['tunningsystem'].Translations["def"]..": "..transla..wheel+1, src = "img/Default.png"})
