local unarmedHash = `WEAPON_UNARMED`
local currWeapon = unarmedHash
local holstered = true
local canFire = true

CreateThread(function()
	while true do
		wait = 3500

		local plyPed = PlayerPedId()
		local exist = DoesEntityExist(plyPed)
		local isDead = IsEntityDead(plyPed)
		local onFoot = IsPedOnFoot(plyPed)
		local parachute = (GetPedParachuteState(plyPed) ~= -1)

		if exist and not isDead and onFoot and not parachute then
			wait = 100

			local newWeap = GetSelectedPedWeapon(plyPed)

			if newWeap ~= currWeapon then
				wait = 5
				
				SetCurrentPedWeapon(plyPed, currWeapon, true)
				local continue = true

				if (weapons[newWeap] ~= nil) and (weapons[newWeap].parameters.onlyBag) then
					local bagIndex = GetPedDrawableVariation(plyPed, 5)

					if not HaveSac(bagIndex) then
						local vehicle = EntityManager:getFrontVehicle(plyPed)

						if DoesEntityExist(vehicle) then
							SetVehicleDoorOpen(vehicle, 5, false, false)

							Wait(2000)

							SetVehicleDoorShut(vehicle, 5, false)
						else
							Framework.ShowNotification("~r~Action Impossible~s~ : Vous devez disposer d'un sac ou être proche d'une voiture.")
							
							continue = false
						end
					end
				end

				if continue then
					wait = 1
					
					local myCoords, myHeading = GetEntityCoords(plyPed), GetEntityHeading(plyPed)
					Framework.Streaming.RequestAnimDict('reaction@intimidation@1h')

					if (weapons[newWeap] ~= nil) and (weapons[newWeap].parameters.anim) then
						if holstered then
							canFire = false

							TaskPlayAnimAdvanced(plyPed, 'reaction@intimidation@1h', 'intro', myCoords, 0, 0, myHeading, 8.0, 3.0, -1, 50, 0, 0, 0)
							
							Wait(1000)

							SetCurrentPedWeapon(plyPed, newWeap, true)

							currWeapon = newWeap

							Wait(2000)

							ClearPedTasks(plyPed)

							holstered = false
							canFire = true
						elseif newWeap ~= currWeapon then
							canFire = false

							TaskPlayAnimAdvanced(plyPed, 'reaction@intimidation@1h', 'outro', myCoords, 0, 0, myHeading, 8.0, 3.0, -1, 50, 0, 0, 0)
							
							Wait(1600)
							
							SetCurrentPedWeapon(plyPed, unarmedHash, true)
							TaskPlayAnimAdvanced(plyPed, 'reaction@intimidation@1h', 'intro', myCoords, 0, 0, myHeading, 8.0, 3.0, -1, 50, 0, 0, 0)
							
							Wait(1000)

							SetCurrentPedWeapon(plyPed, newWeap, true)
							
							currWeapon = newWeap

							Wait(2000)

							ClearPedTasks(plyPed)

							holstered = false
							canFire = true
						end
					else
						if not holstered and (weapons[currWeapon] ~= nil) and (weapons[currWeapon].parameters.anim) then
							canFire = false

							TaskPlayAnimAdvanced(plyPed, 'reaction@intimidation@1h', 'outro', myCoords, 0, 0, myHeading, 8.0, 3.0, -1, 50, 0, 0, 0)
							
							Wait(1600)

							SetCurrentPedWeapon(plyPed, unarmedHash, true)
							ClearPedTasks(plyPed)
							SetCurrentPedWeapon(plyPed, newWeap, true)

							holstered = true
							canFire = true
							currWeapon = newWeap
						else
							SetCurrentPedWeapon(plyPed, newWeap, true)

							holstered = false
							canFire = true
							currWeapon = newWeap
						end
					end

					RemoveAnimDict('reaction@intimidation@1h')
				end
			end
		end

		if not canFire then
			wait = 1
			DisableControlAction(0, 25, true)
			DisablePlayerFiring(plyPed, true)
		end

		Wait(wait)
	end
end)

CreateThread(function()
	local crosshairDisabled = false

	while true do
		wait = 2000

		local plyPed = PlayerPedId()
		if IsPedArmed(plyPed, 6) then
			wait = 1
			
			local weapon = GetSelectedPedWeapon(plyPed)

			SetPlayerLockonRangeOverride(player, 2.0)
			
			DisableControlAction(1, 140, true)
			DisableControlAction(1, 141, true)
			DisableControlAction(1, 142, true)

			if weapons[weapon] ~= nil then
				local isShooting = IsPedShooting(plyPed)
				SetPedInfiniteAmmo(plyPed, weapons[weapon].parameters.infiniteAmmo, weapon)
				SetPedInfiniteAmmoClip(plyPed, weapons[weapon].parameters.infiniteAmmo)

				if crosshairDisabled and not weapons[weapon].parameters.scoped then
					HideHudComponentThisFrame(14)
				end

				if isShooting then
					if weapons[weapon].parameters.shakeCam ~= 0 then
						if not IsPedDoingDriveby(plyPed) then
							CreateThread(function()
								local recoilState = 0

								repeat
									Wait(1)
									local camPitch = GetGameplayCamRelativePitch()

									if GetFollowPedCamViewMode() ~= 4 then
										SetGameplayCamRelativePitch(camPitch + 0.1, 0.2)
									end
									
									recoilState = recoilState + 0.1
								until recoilState >= weapons[weapon].parameters.shakeCam
							end)
						end

						ShakeGameplayCam('SMALL_EXPLOSION_SHAKE', weapons[weapon].parameters.shakeCam)
					end
				end
			end
		end

		Wait(wait)
	end
end)

HaveSac = function(bagIndex)
	local haveBag = false;
	Framework.TriggerServerCallback('skinchanger:getPlayerSkin', function(skin)
		if skin.sex == 0 then
			for k,v in pairs(Config['utils'].BagsList.m) do
				if bagIndex == v then haveBag = true; end
			end
		else
			for k,v in pairs(Config['utils'].BagsList.f) do
				if bagIndex == v then haveBag = true; end
			end
		end
	end)
	Wait(500)
	return haveBag;
end